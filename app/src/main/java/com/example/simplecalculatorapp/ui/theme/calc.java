package com.example.simplecalculatorapp.ui.theme;

public class calc {
    public int add(int num1, int num2) {
        return num1 + num2;
    }

    public int subtract(int num1, int num2) {
        return num1 - num2;
    }
    public int multiply(int num1, int num2) {
        return num1 * num2;
    }
    public int divison(int num1, int num2) {
        return num1/num2;
    }

}
